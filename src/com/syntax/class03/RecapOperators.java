package com.syntax.class03;

public class RecapOperators {

	public static void main(String[] args) {
	
		/*addition => +
		 * subtraction => -
		 * multiplication => *
		 * division=> /
		 * modulus=>%
		 */
		int number1=10;
		int number2=5;
		
        System.out.println(number1/number2);//
        
        double number3=9;
        double number4=2;
        System.out.println("remainder ="+ number3%number4);
        
        System.out.println(number3/number4); 
        // when dividing is better to use double or float// in case there are decimals.
        float number5=1.0f;
        float number6=3.0f;
         System.out.println(number5/number6);
         
         
        
	}

}
